public class BitwiseOperatorTestes {

    public static void main(String[] args) {
        System.out.println(123 | 345);
        System.out.println(123 & 345);
        System.out.println(123 ^ 345);
        System.out.println(~5);
        System.out.println(100 << 2);
        System.out.println(100 >> 2);
    }
}
